/** Helper: genera el array de WorkDay para los 7 días */
const generateWorkDays = (
  start = "06:00",
  lunch = "12:00",
  lunchDuration = 60,
  end = "17:00"
): WorkDay[] => {
  const days = [
    "Monday",
    "Tuesday",
    "Wednesday",
    "Thursday",
    "Friday",
    "Saturday",
    "Sunday",
  ] as const;

  return days.map((day) => ({
    day,
    start_time: start,
    lunch_time: lunch,
    lunch_duration: lunchDuration,
    end_time: end,
  }));
};

